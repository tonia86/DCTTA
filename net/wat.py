    def _reset_parameters(self):
        for m in self.modules():
            if isinstance(m, MSDeformAttn):
                m._reset_parameters()
        normal_(self.level_embed)

    def forward(self, x):
        #x:b,c,h,w
        x = x.permute(0,2,3,1) # bchw -> bhwc
        x_list = []
        for i in range(self.num_levels):
            x_list.append(self.wts[i](x)) #blh'w'c

        for i in range(self.num_levels):
            x_list[i][:,0,...] = self.attns[i](x_list[i][:,0,...])

        srcs = []
        masks = []
        pos_embeds = []
        for i in range(self.num_levels):
            srcs.append(x_list[i][:,0,...].permute(0,3,1,2)) #bchw
            masks.append(torch.zeros((srcs[i].shape[0],srcs[i].shape[2],srcs[i].shape[3]),dtype=torch.bool,device=srcs[i].device))
            pos_embeds.append(self.position_embedding(NestedTensor(srcs[i],masks[i])))
        src_flatten = []
        mask_flatten = []
        lvl_pos_embed_flatten = []
        spatial_shapes = []
        for lvl, (src, mask, pos_embed) in enumerate(zip(srcs, masks, pos_embeds)):
            bs, c, h, w = src.shape
            spatial_shape = (h, w)
            spatial_shapes.append(spatial_shape)
            src = src.flatten(2).transpose(1, 2)
            mask = mask.flatten(1)
            pos_embed = pos_embed.flatten(2).transpose(1, 2)
            lvl_pos_embed = pos_embed + self.level_embed[lvl].view(1, 1, -1)
            lvl_pos_embed_flatten.append(lvl_pos_embed)
            src_flatten.append(src)
            mask_flatten.append(mask)
        src_flatten = torch.cat(src_flatten, 1)
        mask_flatten = torch.cat(mask_flatten, 1)
        lvl_pos_embed_flatten = torch.cat(lvl_pos_embed_flatten, 1)
        spatial_shapes = torch.as_tensor(spatial_shapes, dtype=torch.long, device=src_flatten.device)
        level_start_index = torch.cat((spatial_shapes.new_zeros((1, )), spatial_shapes.prod(1).cumsum(0)[:-1]))
        valid_ratios = torch.stack([get_valid_ratio(m) for m in masks], 1)
        memory = self.deformable_attn(src_flatten, spatial_shapes, level_start_index, valid_ratios, lvl_pos_embed_flatten, mask_flatten)
        start = 0
        for i in range(self.num_levels):
            b,h,w,c = x_list[i][:,0,...].shape
            x_list[i][:,0,...] =  memory[:,start:start+h*w,:].view(b,h,w,c)
            start = start+h*w
        for i in range(self.num_levels):
            x_list[i] = self.iwts[i](x_list[i])
            
        output=sum(x_list)/len(x_list)
        output=output.permute(0,3,1,2)

        return output